package homework;

class MyMath {
	
	long add(long a, long b) {
		long result = a + b;
		return result;
	}
	
	long subtract(long a, long b) {
		long result = a - b;
		return result;
	}
	
	long multiply(long a, long b) {
		long result = a * b;
		return result;
	}
	
	double divide(double a, double b) {
		double result = a / b;
		return result;
	}

}
